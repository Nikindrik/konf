#!/bin/bash

# Проверяем все файлы с расширениями .c, .js и .py в текущей директории
for file in *.c *.js *.py; do

    # Проверяем, существует ли файл, т.к. может не быть файлов с такими расширениями
    if [ -f "$file" ]; then
        
        # Читаем первую строку файла
        first_line=$(head -n 1 "$file")
        
        # Проверяем, начинается ли первая строка с символа комментария
        # Для файлов .c и .js комментарий начинается с "//" или "/*"
        # Для файлов .py комментарий начинается с "#"
        
        # Используем условие для разных типов файлов:
        if [[ "$file" == *.c || "$file" == *.js ]]; then
            if [[ "$first_line" == //* || "$first_line" == '/*'* ]]; then
                echo "Файл $file: комментарий найден в первой строке."
            else
                echo "Файл $file: комментарий отсутствует в первой строке."
            fi
        elif [[ "$file" == *.py ]]; then
            if [[ "$first_line" == \#* ]]; then
                echo "Файл $file: комментарий найден в первой строке."
            else
                echo "Файл $file: комментарий отсутствует в первой строке."
            fi
        fi
    fi
done
